package 반복문;

public class For문연습 {

	public static void main(String[] args) {

		for (int i = 0; i < 10; i++) {
			System.out.print("★");
		}
		System.out.println();
		for (int i = 0; i < 5; i++) {
			System.out.println("커피");
		}
		System.out.println();
		for (int i=0; i<3; i++) {
			System.out.println("커피우유");
		}
		System.out.println();
		
		for (int i = 0; i < 3; i++) {
			System.out.println((i+1) + ": 짱");
		}

	}

}
