package 연산자;

public class 비교연산 {

	public static void main(String[] args) {
		int x =  200;
		int y = 100;
		
		System.out.println("일치?? "+ (x==y));
		System.out.println("불일치?? "+ (x!=y));
		System.out.println("이상?? "+ (x>=y));
		System.out.println("이하?? "+ (x<=y));
		System.out.println("초과?? "+ (x>y));
		System.out.println("미만?? "+ (x<y));

	}

}
